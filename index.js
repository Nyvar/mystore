const productRow = document.getElementById("product-row");

function renderProducts(filteredProducts) {
  productRow.innerHTML = ""; // Clear previous cards
  filteredProducts.forEach(product => {
    const card = document.createElement("div");
    card.className = "col-6 col-md-3 mb-4";
    card.innerHTML = `
      <div class="card h-100 text-center">
        <img src="${product.image}" class="card-img-top" alt="${product.title}">
        <div class="card-body">
          <h5 class="card-title">${product.title}</h5>
          <p class="card-text">${product.price}</p>
        </div>
      </div>
    `;
    productRow.appendChild(card);
  });
}

// Initial render
renderProducts(products);

// Filter on nav click
document.querySelectorAll(".scroll-nav a").forEach(link => {
  link.addEventListener("click", () => {
    const tag = link.getAttribute("data-tag");
    if (tag === "all") {
      renderProducts(products);
    } else {
      const filtered = products.filter(p => p.tag.includes(tag));
      renderProducts(filtered);
    }
  });
});
const searchForm = document.querySelector("form");
const searchInput = document.getElementById("search-input");

searchForm.addEventListener("submit", function (e) {
  e.preventDefault(); // Prevent page reload

  const query = searchInput.value.toLowerCase().trim();

  const filtered = products.filter(product =>
    product.title.toLowerCase().includes(query)
  );

  renderProducts(filtered);
});
